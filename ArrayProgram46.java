package com.pst.Taskk;

import java.util.Vector;

public class ArrayProgram46
{
	public static void main(String[] args)
	{
		Vector<String> vec=new Vector<String>();
		vec.add("M");
		vec.add("A");
		vec.add("N");
		vec.add("I");
		
		System.out.println(vec);
		
		for(String str:vec)
		{
			System.out.println(str);
		}
	}
	

}
